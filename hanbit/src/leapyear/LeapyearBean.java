package leapyear;

public class LeapyearBean {

	private int year, month, days;
	
	public void setYear(int year) {
		this.year = year;
	}
	public int getYear() {
		return year;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getMonth() {
		return month;
	}
	public void setDays(int days) {
		this.days = days;
	}
	public int getDays() {
		return days;
	}
}
